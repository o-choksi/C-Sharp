// Namespace for RapidSpell controls and components
using Keyoti.RapidSpell; // Contains the main classes for adding spell-checking functionality, including the RapidSpellChecker and RapidSpellAsYouType components.

// Namespace for dictionaries used by RapidSpell
using Keyoti.RapidSpell.Dictionaries; // Provides classes and interfaces for managing dictionaries, including custom dictionaries and user dictionaries.

// Namespace for events related to spell checking
using Keyoti.RapidSpell.Events; // Contains event argument classes used in various RapidSpell events like TextBoxSpellCheckComplete and WordIncorrect events.

// Namespace for user interfaces related to spell checking
using Keyoti.RapidSpell.GUI; // Contains classes for GUI components, such as dialog boxes for spell check suggestions and correction options.

// Namespace for managing languages in RapidSpell
using Keyoti.RapidSpell.Language; // Provides support for multiple languages, including classes to manage language-specific dictionaries and settings.

// Namespace for integrating RapidSpell with third-party controls
using Keyoti.RapidSpell.Integration; // Contains classes and interfaces to integrate RapidSpell with third-party controls, like third-party text editors and other custom controls.

// Namespace for handling multiple spell-checking modes
using Keyoti.RapidSpell.SpellingModes; // Provides classes to manage different spell-checking modes, such as as-you-type, batch mode, and real-time spell checking.

// Namespace for RapidSpell's configuration and settings
using Keyoti.RapidSpell.Config; // Contains classes to configure and manage settings related to spell-checking, including settings for dictionaries, user preferences, and spell-check behavior.

// Namespace for handling phonetic algorithms in spell-checking
using Keyoti.RapidSpell.Phonetic; // Provides classes and methods for phonetic spell checking, allowing RapidSpell to suggest corrections based on phonetic similarity.

// Namespace for managing RapidSpell custom word lists
using Keyoti.RapidSpell.CustomDictionaries; // Contains classes for creating and managing custom word lists, enabling the addition of domain-specific or user-specific terms to the spell checker.
